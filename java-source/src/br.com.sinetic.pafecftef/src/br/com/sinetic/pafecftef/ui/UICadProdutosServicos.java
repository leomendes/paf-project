    private String getSituacao(int selectedIndex){
        if(selectedIndex == 0){
            return "I";
        }else if(selectedIndex == 1){
            return "N";   
        }else if(selectedIndex == 2){
            return "F";
        }else if(selectedIndex == 3){
            return "T";
        }else if(selectedIndex == 4){
            return "S";
        }
        return "";
    }
    
    //"Isento", "Não Tributado", "Substituição Tributária", "Tributado pelo ICMS", "Tributado pelo ISSQN"
     private int getIndexSituacao(String status){
        switch (status) {
            case "I":
                return 0;
            case "N":
                return 1;
            case "F":
                return 2;
            case "T":
                return 3;
            case "S":
                return 4;
        }
        return 0;
    }

