    public boolean loadPafProduct(String gtin, String desc, String unid, Double value,String situacao){
        try{
            cleanFields();
            jTextGtin.setText(gtin);
            jTextDesc.setText(desc);
            jTextUnid.setText(unid);
            jTextValue.setText(value.toString());
            jComboSit.setSelectedIndex(getIndexSituacao(situacao));
        }catch(Exception e){
            cleanFields();
            return Boolean.FALSE;
        }
        
        return Boolean.TRUE;
    }
    
    private void cleanFields(){
        jTextGtin.setText(null);
        jTextDesc.setText(null);
        jTextUnid.setText(null);
        jTextValue.setText(null);
    }
    
